    public void listarP1() {
        DefaultTableModel t = (DefaultTableModel) jTable1.getModel();
        Connection con = null;
        try {
            con = ConnectionSGBD.getConnection();
            Statement st = con.createStatement();
            ResultSet rs = st.executeQuery("SELECT F.NRO_REGISTRO, COUNT(*) AS QTDE_BEB_ALCOOLICAS_DIF FROM FESTA F" +
                                            "    JOIN ALIMENTO_FESTA AF ON AF.FESTA_NRO_REGISTRO = F.NRO_REGISTRO " +
                                            "    JOIN ALIMENTO A ON A.NOME = AF.ALIMENTO_NOME " +
                                            "    WHERE A.TIPO_ALIMENTO = 'BEBIDA' AND A.TIPO_COMIDA_BEBIDA = 'ALCOOLICA' " +
                                            "    GROUP BY F.NRO_REGISTRO");

            while (rs.next()) {
                t.addRow(new Object[]{rs.getInt(1), rs.getInt(2)});
            }
            
            //con.commit(); //depois ver de desabilitar commit automatico, mas por enquanto eh melhor assim
        } catch (SQLException e) {
            System.out.println(e.getMessage());
            System.out.println("Erro no SelecionarP1");
        } catch (Exception e) {
            System.out.println(e.getMessage());
            System.out.println("Erro no SelecionarP1");
        }
        
        ConnectionSGBD.CloseConnection(con);

    }

